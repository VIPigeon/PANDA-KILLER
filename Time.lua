    total_time_passed_ms = 0,
    -- –•—Ä–∞–Ω–∏—Ç—Å—è –≤ –º–∏–ª–ª–∏—Å–µ–∫—É–Ω–¥–∞—Ö, –Ω–∞–ø—Ä–∏–º–µ—Ä 16.837
    delta_ms = 0,
