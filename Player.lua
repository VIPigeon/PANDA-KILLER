function player.update(self)
    if self.is_dead then
        return
    end

    -- –ö—Ä–∞—Ç–∫–æ–µ –æ–ø–∏—Å–∞–Ω–∏–µ update() üì∞
    --
    -- 1. –ù–∞ –Ω–∞—á–∞–ª–µ –∫–∞–¥—Ä–∞ –¥–µ–ª–∞–µ–º –Ω–µ—Å–∫–æ–ª—å–∫–æ "–∑–∞–ø—Ä–æ—Å–æ–≤" –∫ —Ñ–∏–∑–∏–∫–µ, —á—Ç–æ–±—ã –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å
    --    –∫–∞–∫–∏–µ —Å—Ç–µ–Ω—ã —Ä—è–¥–æ–º —Å –Ω–∞–º–∏, –Ω–∞ –∑–µ–º–ª–µ –ª–∏ –º—ã, —Ç.–¥.
